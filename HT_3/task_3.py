# 3. Написати функцiю season, яка приймає один аргумент — номер мiсяця (вiд 1 до 12), яка буде повертати пору року, якiй цей мiсяць належить (зима, весна, лiто або осiнь)

def season(n):
    if (n < 12) and (n > 8):
        return 'осінь'
    if (n < 6) and (n > 2):
        return 'весна'
    if (n < 9) and (n > 5):
        return 'літо'
    return 'зима'


n = int(input("Введіть номер місяця: "))
print(season(n))
    
